# Program to find the area of a triangle.
print("Welcome to find area of a triangle.")

# Input the base and height from the user
base_length = float(input("Enter the length of the base of the triangle: "))
height = float(input("Enter the height of the triangle: "))

# Calculate the area of the triangle.
calculate_area = base_length * height * 0.5

# Display the result.
print(f"The area of the triangle is: {calculate_area}")